    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maKH = txtMaKH.getText().toString();
        String tenKH = txtTenKH.getText().toString();
        String diaChi = txtDiaChi.getText().toString();
        String dienThoai = txtDienThoai.getText().toString();
        
        if (maKH.equals(null) || tenKH.equals(null) ||diaChi.equals(null) ||dienThoai.equals(null)){
            ThongBao tb = new ThongBao(Contants.ERROR_FULL_FIELD);
            tb.show();
        } else {
            KhachHang kh = new KhachHang(maKH, tenKH, diaChi, dienThoai);
            int res = new DbKhachHang().insertKhachHang(kh);
            if (res >0){
                ThongBao tb = new ThongBao(Contants.INSERT_SUCCESS);
                tb.show();
                
                ArrayList<KhachHang> listKH = new DbKhachHang().getListKhachHang();
                updateTable(listKH);
                
            } else {
                ThongBao tb = new ThongBao(Contants.INSERT_FAIL);
                tb.show();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

