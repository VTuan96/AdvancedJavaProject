    public void updateTable(ArrayList<KhachHang> list) {
        //cap nhat lai model cho table
        DefaultTableModel model=(DefaultTableModel) tbKhachHang.getModel();
        model.setRowCount(0); //set so dong = 0
        model.setColumnCount(0); //set so cot =0
        
        //them cot vao model
        for( int i=0 ; i<Contants.columnKH.length ; i++){
            model.addColumn(Contants.columnKH[i]);
        }
        
        //them dong vao model
        Object[] row=new Object[Contants.columnKH.length];
        for (int j=0 ; j<list.size() ; j++){
            //k cot
            for (int k=0 ; k<Contants.columnKH.length ; k++){
                if(k==0) //cot 1, ma khach hang
                {
                    row[k]=list.get(j).getMaKhachHang();
                } 
                else if (k==1) // cot 2, Ten khach hang
                {
                    row[k]= list.get(j).getTenKhachHang();
                } 
                else if (k==2) //dia chi
                    row[k] = list.get(j).getDiaChi();
                else if (k==3) //So dien thoai
                    row[k] = list.get(j).getDienThoai();
                         
            }              
            model.addRow(row);
        }
    }
    
